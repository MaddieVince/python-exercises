# Generate lists and outputs based on conditions

foods = ["orange", "apple", "banana", "strawberry", "grape", "blueberry",
["carrot", "cauliflower", "pumpkin"], "passionfruit", "mango", "kiwifruit"]

#print the first item in the list
print(foods[0])

#print the third item in the list
print(foods[2])

#print the last item in the list
print(foods[-1])

#print the first three items in the list
print(foods[0:3])

#print the last three items in the list
print(foods[-3:])

#print the last item in the sublist
print(foods[-4][-1])
